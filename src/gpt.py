import aiohttp
from src.config import GPT_TOKEN


url = "https://llm-proxy.t-tech.team/chat/completions"
headers = {
    "Content-Type": "application/json",
    "Authorization": f"Bearer {GPT_TOKEN}"
}


async def summarize_dialog(text: str, promt: str = "") -> str:
    promt = f"Дополнительный промпт от пользователя: {promt}" if promt else ""
    data = {
        "model": "tgpt/gemma-3-27b-it",
        "max_tokens": 1500,
        "presence_penalty": -1,
        "top_p": 0.2,
        "messages": [
            {
                "role": "system",
                "content": (
                    "На основе предоставленных сообщений из треда составь краткий пересказ обсуждения на русском языке.\n"
                    "Требования:\n"
                    "– Выдели ключевые вопросы пользователя и финальные ответы/решения со стороны поддержки.\n"
                    "– Исключи неинформативные реплики (например, приветствия, благодарности).\n"
                    "– Сохрани хронологическую последовательность основных смысловых шагов.\n"
                    "– Будь кратким, но точным: цель — быстро понять суть обсуждения без чтения всего треда.\n"
                    "– Если в треде есть итоговое решение проблемы — укажи его отдельно в конце.\n"
                    f"{promt}"
                )
            },
            {
                "role": "user",
                "content": text,
            }
        ]
    }
    return await gpt_request(data)


async def create_answer_with_gpt(question: str, text: str) -> str:
    system_prompt = (
        "ОБЩИЕ ПРИНЦИПЫ:\n"
        "- Ты бот, который на основе предыдущего схожего треда отвечает на новый вопрос. "
        "В старом обсуждении может не быть ответа на новый вопрос.\n"
        "- Нельзя аппрувить никакие действия пользователя, выражать мнение, делать выводы о приоритетах задач. "
        "Можно только давать конкретные ответы на вопросы.\n"
        "- Не придумывай данные, события, источники, не связывай старые события как причину нового сбоя.\n"
        "ИНСТРУКЦИЯ:\n"
        "1. В первую очередь опирайся на представленные сообщения из треда.\n"
        "2. Если в треде есть полный или частичный ответ на вопрос пользователя, используй эту информацию и ответь на вопрос.\n"
        "3. Если в треде нет нужной информации, но ты можешь ответить сам без выдумывания фактов, ответь. "
        "Иначе пришли ровно строку '*Ошибка*.'.\n"
        "4. Нельзя писать, что вопрос снят, обращение обработано, неактуально, нельзя ничего аппрувить. "
        "Если в предыдущем обсуждении пользователь сам закрыл вопрос, пришли только решение, которое к этому привело.\n"
        "5. Не цитируй и не пересказывай текст вопроса и треда, не вставляй их части в ответ.\n"
        "6. Ответ должен содержать только суть решения/диагностики, без вводных фраз и рассуждений о том, как ты думал.\n"
        "7. Ответ не должен превышать 1000 символов и 5 предложений.\n"
        "8. Если ты не можешь дать конкретный ответ или план действий, пришли ровно строку '*Ошибка*.'.\n"
    )

    data = {
        "model": "tgpt/t-pro-it-2-0-fp8",
        "max_tokens": 600,
        "presence_penalty": -1,
        "top_p": 0.2,
        "messages": [
            {
                "role": "system",
                "content": system_prompt,
            },
            {
                "role": "system",
                "content": f"Тред с, возможно, похожим вопросом и обсуждением:\n{text}",
            },
            {
                "role": "user",
                "content": (
                    f"Новый вопрос, на который надо ответить:\n{question}\n"
                    "Ответь строго по инструкции выше."
                ),
            },
        ],
        "chat_template_kwargs": {
            "enable_thinking": False,
        },
    }
    return await gpt_request(data)


async def gpt_request(data: dict) -> str:
    timeout = aiohttp.ClientTimeout(total=350)
    async with aiohttp.ClientSession(timeout=timeout) as session:
        async with session.post(url, json=data, headers=headers, ssl=False) as response:
            response.raise_for_status()
            result = await response.json()
            result = result['choices'][-1]['message']['content'].replace('@', '')
            return result
